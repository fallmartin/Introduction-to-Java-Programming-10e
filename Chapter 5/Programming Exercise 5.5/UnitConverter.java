public class UnitConverter {
    // ï»¿Conversion from kilograms to pounds and pounds to kilograms
    public static void main(String[] args) {
        final double KILOGRAMS_TO_POUNDS = 2.2;
        System.out.print("Kilograms      Pounds");
        System.out.print("     |     ");
        System.out.println("Pounds      Kilograms");
        
        for (int i = 1, j = 20; i < 200; i +=2, j += 5) {
            System.out.printf("%-10d %10.1f", i, i * KILOGRAMS_TO_POUNDS);
            System.out.print("           ");
            System.out.printf("%-10d %10.2f\n", j, j / KILOGRAMS_TO_POUNDS);
        }
    }
}
